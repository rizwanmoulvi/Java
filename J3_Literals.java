//Values that are stored in variables are called literals.
//adding f or d infront of a number makes it a float or double in decimal
//adding l infront of a number makes it a long

public class J3_Literals {
    public static void main(String[] args) {
        //integer literal
        byte a = 5;
        short b = 10;
        int c = 20;
        //long literal
        long d = 100000000000L;
        //decimal literal
        float e = 10.5f;
        double f = 20.5d; //d is optional
        //character literal
        char g = 'g';
        //string literal
        String h = "Hello";
    }
}